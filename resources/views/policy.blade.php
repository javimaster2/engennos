<ol>
<li>Proporcionar información que el usuario facilite.</li>
<li>Notificar al usuario sobre cambios en la plataforma.</li>
<li>Permitir al usuario participar en actividades interactivas de la plataforma.</li>
<li>Brindar soporte al usuario.</li>
<li>Validar la veracidad de la información brindada.</li>
<li>Atender las consultas y solicitudes del usuario.</li>
<li>Gestionar las preferencias del usuario referente a su cuenta.</li>
<li>Realizar estudios estadísticos para potenciar el funcionamiento de los servicios prestados por Engennos y lograr mejorar la experiencia de usuario, así como tendencias, tráfico y realizar seguimiento de compras.</li>
<li>Garantizar el cumplimiento de los protocolos de seguridad y otros aspectos relacionados a los servicios brindados por Codersfree.</li>
<li>Facilitar el funcionamiento técnico de los servicios, lo cual incluye solución de problemas, seguridad de los servicios, prevención de fraude y uso indebido del contenido de la empresa.</li>
<ol>
Engennos no divulga con ningún tercero tu información, pues su uso está restringido únicamente a nuestro servicio. Asimismo, debemos acotar que los datos recopilados serán recopilados para las siguientes situaciones:
<ol>
<li>
<b>Administración del sistema:</b> Podemos usar los datos personales del usuario para la administración de nuestros sistemas, el diagnóstico de problemas con los servidores, supervisión del rendimiento, tráfico en nuestro sitio web y aplicación y obtener información demográfica.
</li>
<li>
<b>Administración del sistema:</b> La información personal del usuario será usada para enviar a su correo nuestros anuncios, ofertas y publicidad sobre los productos ofrecidos por la plataforma. El usuario expresará su total consentimiento de recibir este tipo de contenido, ya que al registrarse puede activar en el "check box" la opción "Acepto recibir material publicitario y/o comercial de Codersfree". Si luego el usuario quiere desistir de este servicio puede oprimir la opción "Darse de baja".
</li>
<li>
<b>Encuestas:</b> A través del correo electrónico el usuario o cualquier otro medio disponible el usuario recibirá encuestas sobre nuestros servicios y/o cualquier otro tema de interés con el fin de mejorar nuestros servicios.
</li>
<li>
<b>Facturación:</b> Usaremos los datos personales del usuario con el fin de crear un registro y un historial de las compras realizadas a la empresa.
</li>
</ol>
