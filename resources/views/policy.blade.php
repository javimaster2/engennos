                <h1>Política de privacidad</h1>
<p>Engennos pretende ofrecer la mejor experiencia de compra y estudio a cada uno de sus usuarios. Por ello nos esforzamos encarecidamente en proteger su privacidad presentando este documento donde detallamos nuestros procedimientos y políticas respecto a la información y datos personales. Datos que son recopilados a través del sitio web y/o plataformas virtuales de Engennos.</p>
<p>Los usuarios que acceden a nuestros servicios a través de la plataforma aceptan estar sujetos a las disposiciones de nuestra Política de Privacidad. Acepta que se recopile su información personal a través de nuestras encuestas, formularios electrónicos, entre otros, bien sea otorgados y/o requeridos de forma virtual por la plataforma.</p>
<p>La empresa garantiza que la información personal brindada cuenta con la seguridad necesaria y no será entregada a terceros. Solo se requerirán o validarán datos en casos de incumplimiento de alguna orden judicial o requerimientos legales.</p>
<p>La suscripción a boletines de correos electrónicos es voluntaria y se le dará la opción de seleccionarla al momento de crear su cuenta y en cualquier momento usted podrá darse de baja.</p>
<p>Es necesario acotar que la presente política forma parte de nuestros Términos y Condiciones que podrán encontrar en Terminos y condiciones de uso. Asimismo, la empresa se reserva los derechos de modificar la Política de Privacidad sin previo aviso.</p>
<h2>¿Qué información recopilamos?</h2>
Engennos recopila el siguiente tipo de información:
<ol>
<li>
Los datos que nos envías al momento de registrarte en nuestra plataforma o al tratar de comunicarte con nosotros. Por ejemplo: el correo electrónico, fechas de nacimiento, nombres completos, entre otros.
</li>
<li>
Información sobre las páginas de la empresa a la que accedes, en este caso serían los datos sobre tu dispositivo: sistema operativo o tipo de navegador.
</li>
<li>
Recolectaremos tus datos de usuario en cuanto al contenido compartido dentro de nuestra plataforma, las páginas de Engennos a las que se accede, los datos de cursos (comportamiento, uso del producto, comentarios, proyectos creados), datos de pago y datos sobre las compras.
</li>
</ol>
Es importante destacar que nuestra tecnología nos permite identificar datos como la ubicación geográfica del usuario (país y ciudad) e historial de navegación por nuestro sitio web y aplicación. Esto en caso de reportar un problema y tengamos la necesidad de ver cuáles páginas ha usado el usuario e identificar mejor su caso.
<p>Por motivos de seguridad no almacenamos los datos confidenciales como el titular de una tarjeta, entiéndase los números de tarjeta de crédito o datos de autenticación de la misma; datos de los usuarios y sus cuentas en otros servicios, salvo se encuentren directamente vinculados a Engennos.</p>
<p>Al realizar un pago a través de alguna de nuestras pasarelas de pago usted aceptará la política de privacidad correspondiente a dicha pasarela. Engennos no tiene acceso a ninguno de los datos que el usuario comparta con la entidad bancaria por la que decida realizar el pago.</p>
<p>El usuario tiene la opción de añadir información a su perfil, nos referimos a incluir una foto de perfil, una breve descripción, país y ciudad donde vive o proviene y fecha de nacimiento.</p>
